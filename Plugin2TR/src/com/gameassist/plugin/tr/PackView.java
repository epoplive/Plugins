	private int itemCount = 1;

	@Override
	public void onClick(View v) {
		// TODO Auto-generated method stub
		switch (v.getId()) {
		case R.id.packBtnAddItem:
			mList0 = mPackGridViewAdapter.getItemList();
			String itemnames = "";
			int isSuccess = 0;
			if (mList0 != null) {
				for (PackItem pis : mList0) {
					isSuccess  = NativeUtils.nativeDoAddItemCheat(1, pis.getItemId(),itemCount);
					if (isSuccess == 0) {
						Toast.makeText(getContext(),  pis.getItemName()+"添加失败", Toast.LENGTH_SHORT)
								.show();
						break;
					}
					itemnames = itemnames + pis.getItemName() + ":" + itemCount+ "\n";
					Log.i("gameassist","name:" + pis.getItemName() + pis.getItemId()+ mList0.size());
				}
				if(isSuccess==1){
					Toast.makeText(getContext(), itemnames + "添加成功",
							Toast.LENGTH_SHORT).show();
				}
			} else {
				Toast.makeText(getContext(), "没有要添加的物品", Toast.LENGTH_SHORT)
						.show();
			}
			break;
		case R.id.packSearch:
			serachString = mEditText.getText().toString()
			.replaceAll("\\s*", "");
			Log.i("gameassist", serachString);
			for (PackItem pis : mItemStacks) {
				if (pis.getItemName().contains(serachString)) {
					searchItems.add(pis);
				}
			}
			mPackGridViewAdapter.addDatas(searchItems);
			searchItems.clear();
			break;
		case R.id.packClear:
			mEditText.setText("");
			mPackGridViewAdapter.clearSelected();
			break;
			
		case R.id.packEditText:
			break;
			
		default:
			break;
		}
	}

